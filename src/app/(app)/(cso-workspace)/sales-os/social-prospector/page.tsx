function CSVImporter({ workspaceId, open, onClose, onImport }: { workspaceId: string; open: boolean; onClose: () => void; onImport: () => void }) {
  const [csvText, setCsvText] = useState("");
  const [loading, setLoading] = useState(false);

  async function handleImport() {
    if (!csvText.trim()) { toast.error("Collez vos données CSV"); return; }
    setLoading(true);
    const lines = csvText.trim().split("\n");
    const interactions: RawInteraction[] = [];
    for (const line of lines.slice(1)) {
      const [platform, type, sourceUrl, name, handle, profileUrl, text] = line.split(",").map((s) => s.trim().replace(/^"|"$/g, ""));
      if (platform && name && handle) {
        interactions.push({
          platform: (platform.toUpperCase() as SocialPlatform) || "INSTAGRAM",
          type: (type?.toUpperCase() as "LIKE" | "COMMENT" | "FOLLOW") || "COMMENT",
          sourceUrl: sourceUrl || "", prospectName: name, prospectHandle: handle,
          profileUrl: profileUrl || undefined, interactionText: text || undefined,
        });
      }
    }
    if (interactions.length === 0) { toast.error("Aucune interaction valide"); setLoading(false); return; }
    const result = await importInteractionsAction(workspaceId, interactions);
    if (result.success) { toast.success(`${result.data?.imported} importées`); setCsvText(""); onClose(); onImport(); }
    else toast.error(result.error || "Erreur");
    setLoading(false);
  }

  return (
    <Dialog open={open} onOpenChange={onClose}>
      <DialogContent className="max-w-2xl">
        <DialogHeader>
          <DialogTitle>Importer des interactions (CSV)</DialogTitle>
          <DialogDescription>Colonnes : platform, type, sourceUrl, prospectName, prospectHandle, profileUrl, interactionText</DialogDescription>
        </DialogHeader>
        <textarea value={csvText} onChange={(e) => setCsvText(e.target.value)} placeholder={`platform,type,sourceUrl,prospectName,prospectHandle,profileUrl,interactionText\nINSTAGRAM,COMMENT,https://instagram.com/p/123,Marie Dupont,@marie.dupont,,Super !`} rows={8} className="w-full p-3 border border-gray-200 rounded-lg text-sm font-mono bg-gray-50 focus:ring-2 focus:ring-emerald-500" />
        <div className="flex justify-end gap-3">
          <Button variant="outline" onClick={onClose}>Annuler</Button>
          <Button onClick={handleImport} disabled={loading} className="bg-gradient-to-r from-emerald-600 to-teal-600 text-white">
            {loading ? <Loader2 className="h-4 w-4 mr-2 animate-spin" /> : <Upload className="h-4 w-4 mr-2" />}Importer
          </Button>
        </div>
      </DialogContent>
    </Dialog>
  );
}

