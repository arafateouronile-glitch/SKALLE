function ProspectsTab({ workspaceId }: { workspaceId: string }) {
  const [interactions, setInteractions] = useState<Interaction[]>([]);
  const [stats, setStats] = useState<Stats>({ PENDING: 0, CONTACTED: 0, IGNORED: 0 });
  const [loading, setLoading] = useState(true);
  const [filterStatus, setFilterStatus] = useState("ALL");
  const [filterPlatform, setFilterPlatform] = useState("ALL");
  const [showImport, setShowImport] = useState(false);

  const load = useCallback(async () => {
    setLoading(true);
    const result = await getInteractionsAction(workspaceId);
    if (result.success && result.data) {
      setInteractions(result.data.interactions as unknown as Interaction[]);
      setStats(result.data.stats as Stats);
    }
    setLoading(false);
  }, [workspaceId]);

  useEffect(() => { load(); }, [load]);

  const filtered = interactions.filter((i) => {
    if (filterStatus !== "ALL" && i.status !== filterStatus) return false;
    if (filterPlatform !== "ALL" && i.platform !== filterPlatform) return false;
    return true;
  });

  return (
    <div className="space-y-6">
      {/* Stats */}
      <div className="grid grid-cols-3 gap-4">
        {[
          { label: "En attente", value: stats.PENDING, icon: Clock, color: "amber" },
          { label: "Contactés", value: stats.CONTACTED, icon: CheckCircle2, color: "green" },
          { label: "Total", value: stats.PENDING + stats.CONTACTED + stats.IGNORED, icon: Users, color: "gray" },
        ].map((s) => (
          <Card key={s.label} className="bg-white/60 backdrop-blur-sm shadow-sm border-gray-200/60">
            <CardContent className="p-4 flex items-center gap-3">
              <div className={`p-2.5 rounded-xl bg-${s.color}-50 border border-${s.color}-200/40`}>
                <s.icon className={`h-5 w-5 text-${s.color}-500`} />
              </div>
              <div>
                <p className="text-2xl font-bold text-gray-900">{s.value}</p>
                <p className="text-xs text-gray-500">{s.label}</p>
              </div>
            </CardContent>
          </Card>
        ))}
      </div>

      {/* Tracker */}
      <PostTracker workspaceId={workspaceId} onTrack={load} />

      {/* Filtres */}
      <div className="flex items-center gap-3">
        <Select value={filterStatus} onValueChange={setFilterStatus}>
          <SelectTrigger className="w-[160px] bg-white/60 border-gray-200"><SelectValue /></SelectTrigger>
          <SelectContent>
            <SelectItem value="ALL">Tous les statuts</SelectItem>
            <SelectItem value="PENDING">En attente</SelectItem>
            <SelectItem value="CONTACTED">Contactés</SelectItem>
            <SelectItem value="IGNORED">Ignorés</SelectItem>
          </SelectContent>
        </Select>
        <Select value={filterPlatform} onValueChange={setFilterPlatform}>
          <SelectTrigger className="w-[160px] bg-white/60 border-gray-200"><SelectValue /></SelectTrigger>
          <SelectContent>
            <SelectItem value="ALL">Toutes</SelectItem>
            <SelectItem value="INSTAGRAM">Instagram</SelectItem>
            <SelectItem value="FACEBOOK">Facebook</SelectItem>
          </SelectContent>
        </Select>
        <Button variant="ghost" size="sm" onClick={load} className="text-gray-500">
          <RefreshCw className="h-4 w-4 mr-1" /> Actualiser
        </Button>
        <Button variant="outline" size="sm" onClick={() => setShowImport(true)} className="ml-auto">
          <Upload className="h-4 w-4 mr-1" /> Import CSV
        </Button>
        <IGExtractorHelp workspaceId={workspaceId} onImport={load} />
      </div>

      {/* Liste */}
      {loading ? (
        <div className="flex items-center justify-center py-20">
          <Loader2 className="h-8 w-8 animate-spin text-emerald-400" />
        </div>
      ) : filtered.length === 0 ? (
        <EmptyState message="Aucune interaction trouvée" cta="Tracker un post" onCta={() => {}} />
      ) : (
        <div className="grid grid-cols-1 md:grid-cols-2 xl:grid-cols-3 gap-4">
          {filtered.map((i) => (
            <ProspectCard key={i.id} interaction={i} workspaceId={workspaceId} onUpdate={load} />
          ))}
        </div>
      )}

      <CSVImporter workspaceId={workspaceId} open={showImport} onClose={() => setShowImport(false)} onImport={load} />
    </div>
  );
}

