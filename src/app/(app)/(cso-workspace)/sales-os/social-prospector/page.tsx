// 🧱 ONGLET 2 : QUEUE D'APPROBATION
// ═══════════════════════════════════════════════════════════════════════════

function ApprovalQueueTab({ workspaceId }: { workspaceId: string }) {
  const [pending, setPending] = useState<Interaction[]>([]);
  const [loading, setLoading] = useState(true);
  const [selected, setSelected] = useState<Set<string>>(new Set());
  const [editingId, setEditingId] = useState<string | null>(null);
  const [editText, setEditText] = useState("");

  const load = useCallback(async () => {
    setLoading(true);
    const result = await getApprovalQueueAction(workspaceId);
    if (result.success && result.data) {
      setPending(result.data.pending as unknown as Interaction[]);
    }
    setLoading(false);
  }, [workspaceId]);

  useEffect(() => { load(); }, [load]);

  async function handleApprove(id: string) {
    const res = await approveDMAction(workspaceId, id);
    if (res.success) { toast.success("DM approuvé !"); load(); }
    else toast.error(res.error || "Erreur");
  }

  async function handleReject(id: string) {
    const res = await rejectDMAction(workspaceId, id);
    if (res.success) { toast.success("DM rejeté"); load(); }
    else toast.error(res.error || "Erreur");
  }

  async function handleApproveWithEdit(id: string) {
    if (!editText.trim()) return;
    const res = await approveWithEditAction(workspaceId, id, editText);
    if (res.success) { toast.success("DM modifié et approuvé !"); setEditingId(null); setEditText(""); load(); }
    else toast.error(res.error || "Erreur");
  }

  async function handleBatchApprove() {
    if (selected.size === 0) return;
    const res = await batchApproveDMsAction(workspaceId, Array.from(selected));
    if (res.success) { toast.success(`${selected.size} DM approuvés !`); setSelected(new Set()); load(); }
    else toast.error(res.error || "Erreur");
  }

  function toggleSelect(id: string) {
    const s = new Set(selected);
    s.has(id) ? s.delete(id) : s.add(id);
    setSelected(s);
  }

  function selectAll() {
    setSelected(new Set(pending.map((p) => p.id)));
  }

  function getDMPreview(interaction: Interaction): string {
    const dms = interaction.suggestedDMs;
    if (!dms || dms.length === 0) return "Pas de DM généré";
    const idx = interaction.selectedDmIndex ?? 0;
    return dms[idx]?.message || dms[0]?.message || "Pas de DM";
  }

  if (loading) return <div className="flex items-center justify-center py-20"><Loader2 className="h-8 w-8 animate-spin text-emerald-400" /></div>;

  if (pending.length === 0) {
    return <EmptyState message="Aucun DM en attente d'approbation" cta="Voir les prospects" onCta={() => {}} />;
  }

  return (
    <div className="space-y-4">
      {/* Barre d'actions batch */}
      <div className="flex items-center justify-between p-3 bg-white/60 backdrop-blur-sm rounded-lg border border-gray-200/60">
        <div className="flex items-center gap-3">
          <Button variant="outline" size="sm" onClick={selectAll}>
            <CheckSquare className="h-4 w-4 mr-1" /> Tout sélectionner
          </Button>
          <span className="text-sm text-gray-500">{selected.size} sélectionné(s) sur {pending.length}</span>
        </div>
        <Button
          onClick={handleBatchApprove}
          disabled={selected.size === 0}
          size="sm"
          className="bg-gradient-to-r from-emerald-600 to-teal-600 hover:from-emerald-700 hover:to-teal-700 text-white"
        >
          <Check className="h-4 w-4 mr-1" /> Approuver la sélection
        </Button>
      </div>

      {/* Liste des DM en attente */}
      <div className="space-y-3">
        {pending.map((interaction) => {
          const TypeIcon = typeIcons[interaction.type] || MessageCircle;
          const PlatformIcon = platformIcons[interaction.platform] || Instagram;
          const isEditing = editingId === interaction.id;

          return (
            <Card key={interaction.id} className={`bg-white/60 backdrop-blur-sm shadow-sm border-gray-200/60 ${selected.has(interaction.id) ? "ring-2 ring-emerald-500" : ""}`}>
              <CardContent className="p-4">
                <div className="flex items-start gap-4">
                  {/* Checkbox */}
                  <input
                    type="checkbox"
                    checked={selected.has(interaction.id)}
                    onChange={() => toggleSelect(interaction.id)}
                    className="mt-1 h-4 w-4 rounded border-gray-300 text-emerald-600 focus:ring-emerald-500"
                  />

                  {/* Avatar */}
                  <div className="h-10 w-10 rounded-full bg-gradient-to-br from-violet-500 to-fuchsia-500 flex items-center justify-center text-white font-bold text-sm shrink-0">
                    {interaction.prospectName.split(" ").map((n) => n[0]).join("").slice(0, 2).toUpperCase()}
                  </div>

                  {/* Info */}
                  <div className="flex-1 min-w-0">
                    <div className="flex items-center gap-2 mb-1">
                      <h4 className="font-semibold text-gray-900 text-sm">{interaction.prospectName}</h4>
                      <PlatformIcon className="h-3.5 w-3.5 text-gray-400" />
                      <TypeIcon className="h-3.5 w-3.5 text-gray-400" />
                    </div>
                    {interaction.interactionText && (
                      <p className="text-xs text-gray-500 italic mb-2 truncate">&ldquo;{interaction.interactionText}&rdquo;</p>
                    )}

                    {/* DM Preview */}
                    {isEditing ? (
                      <div className="space-y-2">
                        <textarea
                          value={editText}
                          onChange={(e) => setEditText(e.target.value)}
                          rows={3}
                          className="w-full p-2 border border-gray-200 rounded-lg text-sm focus:ring-2 focus:ring-emerald-500 focus:border-emerald-500"
                        />
                        <div className="flex gap-2">
                          <Button size="sm" onClick={() => handleApproveWithEdit(interaction.id)} className="bg-emerald-600 hover:bg-emerald-700 text-white">
                            <Check className="h-3 w-3 mr-1" /> Approuver
                          </Button>
                          <Button variant="ghost" size="sm" onClick={() => { setEditingId(null); setEditText(""); }}>
                            Annuler
                          </Button>
                        </div>
                      </div>
                    ) : (
                      <div className="p-2 rounded bg-emerald-50/50 border border-emerald-200/30">
                        <p className="text-sm text-gray-700">{getDMPreview(interaction)}</p>
                      </div>
                    )}
                  </div>

                  {/* Actions */}
                  {!isEditing && (
                    <div className="flex items-center gap-1 shrink-0">
                      <Button
                        variant="ghost"
                        size="icon"
                        onClick={() => handleApprove(interaction.id)}
                        className="h-8 w-8 text-green-600 hover:bg-green-50"
                        title="Approuver"
                      >
                        <Check className="h-4 w-4" />
                      </Button>
                      <Button
                        variant="ghost"
                        size="icon"
                        onClick={() => { setEditingId(interaction.id); setEditText(getDMPreview(interaction)); }}
                        className="h-8 w-8 text-blue-600 hover:bg-blue-50"
                        title="Modifier"
                      >
                        <Edit3 className="h-4 w-4" />
                      </Button>
                      <Button
                        variant="ghost"
                        size="icon"
                        onClick={() => handleReject(interaction.id)}
                        className="h-8 w-8 text-red-600 hover:bg-red-50"
                        title="Rejeter"
                      >
                        <X className="h-4 w-4" />
                      </Button>
                    </div>
                  )}
                </div>
              </CardContent>
            </Card>
          );
        })}
      </div>
    </div>
  );
}

